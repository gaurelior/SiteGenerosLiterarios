        <h1 style="text-align: center;"> Quais são os tipos? </h1>
        
    </div>
        <div  id="container">

          <div id="box-1" class="box"><h1 style="text-align: center;">Lírico</h1> <p style="text-align:center">gênero Lírico se refere a poesia, como versos, estrofes, rimas, etc. Tem como objetivo , despertar sensações, emoções e sentimentos, tanto bons e alegres, quanto tristes e ruins.
            Este gênero apresenta linguagem conotativa, é escrito em primeira pessoa. </p></div>
          <div id="box-2" class="box"> <h1 style="text-align: center;">Narrativo </h1>   <p style="text-align:center;"> O Gênero narrativo refere-se aos textos narrados, em outras palavras, que contam uma história, que precisam de narradores.
            Os textos narrativos, tem necessariamente que conter onde ocorre a cena, quem são os personagens, quando ocorre, como acontece, o que se passa e o porquê da cena. 
          </p> </div>
          <div id="box-3" class="box"><h1 style="text-align: center;">Dramático</h1>
          <p style="text-align:center;">O gênero dramático é escrito para que seja encenado, contempla a arte de encenar a partir da interpretação. Tem como objetivo a maior apropriação da obra pelos autores.</p></div>
        </div>
        
        <div  id="container">

          <div id="box-4" class="box"><h1 style="text-align: center;">Exemplos:</h1> <p style="text-align:center">  ◉  Trovas : um poema de quatro versos, que são interpretados por trovadores e seu conteúdo é composto por amor, amizade e algumas vezes ódio, ou seja, de escárnio;
           <p> ◉  Músicas: arte de conduzir efeitos sonoros e harmoniosos, podendo transmitir através de voz ou instrumentos. </p></div>
          <div id="box-5" class="box"> <h1 style="text-align: center;">Exemplos: </h1>   <p style="text-align:center;">  ◉  Fábulas: apresenta animais que agem como humanos, e uma lição de moral;</p>
         <p>   ◉ Romances: contém uma narrativa longa, com fatos relacionados aos personagens. Os mesmos passam por situações dramática e conflitos;</p>
            <p>◉ Lendas: narrativas normalmente orais que explicam acontecimentos sobrenaturais. 
            entre outros etc.       </p> </div>
          <div id="box-6" class="box"><h1 style="text-align: center;">Exemplos:</h1>
